    private void loadRole() {
        ArrayList<Role> lstRole = new ArrayList<>();
        lstRole.removeAll(lstRole);
        DefaultComboBoxModel<Role> dcbm = new DefaultComboBoxModel<Role>();
        ResultSet rs = DConnection.executeQuery("select*from tblRole");
        try {
            while (rs.next()) {
                Role r = new Role();
                r.setId(rs.getInt("id"));
                r.setName(rs.getString("name"));
                lstRole.add(r);
                dcbm.addElement(r);
            }
        } catch (SQLException ex) {
            Logger.getLogger(IFEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbxRole.setModel(dcbm);
    }

    private void loadList(String name) {
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        model.setRowCount(0);
        EmployeeDAOImp empDAO = new EmployeeDAOImp();
        List<Employee> lstEmp = new ArrayList<>(empDAO.lstEmployee(name));
        for (Employee e : lstEmp) {
            String status = "";
            if (e.isStatus() == true) {
                status = "Đang làm";
            } else {
                status = "Nghỉ";
            }
            model.addRow(new Object[]{e.getId(), e.getName(), e.getAge(), e.getEmail(),
                e.getPhone(), e.getAddress(), e.getRoleName(), new Date(e.getStartDate().getTime() + 172800000), e.getWorkTime(),
                e.getRate(), e.getSalary(), e.getPassword(), status});
        }
        tblEmployee.setModel(model);
    }

    private void detail() {
        int selectRow = tblEmployee.getSelectedRow();
        if (selectRow >= 0) {
            int id = (int) tblEmployee.getValueAt(selectRow, 0);
            EmployeeDAOImp empDAO = new EmployeeDAOImp();
            Employee e = new Employee();
            RoleDAOImp roleDAO = new RoleDAOImp();
            e = empDAO.find(id);
            txtName.setText(e.getName());
            txtAge.setText(String.valueOf(e.getAge()));
            txtEmail.setText(e.getEmail());
            txtPhone.setText(e.getPhone());
            txtAddress.setText(e.getAddress());
            cbxRole.getModel().setSelectedItem((Object) roleDAO.find(e.getRoleId()));
            startDate.setDate(new Date(e.getStartDate().getTime() + 172800000));
            txtWT.setText(String.valueOf(e.getWorkTime()));
            txtRate.setText(String.valueOf(e.getRate()));
            txtSalary.setText(String.valueOf(e.getSalary()));
            txtPass.setText(e.getPassword());
            if (e.isStatus()) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }

            this.id = e.getId();
        }
    }

