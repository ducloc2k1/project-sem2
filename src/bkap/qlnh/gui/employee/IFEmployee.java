    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (txtName.getText().trim().isEmpty() && txtAge.getText().trim().isEmpty()
                && txtEmail.getText().trim().isEmpty() && txtPhone.getText().trim().isEmpty()
                && txtAddress.getText().trim().isEmpty() && txtWT.getText().trim().isEmpty()
                && txtRate.getText().trim().isEmpty() && txtSalary.getText().trim().isEmpty()
                && txtPass.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập đủ các trường");
        } else if (txtName.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập tên");
        } else if (txtAge.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập tuổi");
        } else if (!v.CheckNumeric(txtAge.getText())) {
            lbMess.setText("Vui lòng nhập số");
        } else if (txtEmail.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập email");
        } else if (!v.CheckEmail(txtEmail.getText())) {
            lbMess.setText("Email không hợp lệ");
        } else if (txtPhone.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập sđt");
        } else if (!v.CheckPhone(txtPhone.getText())) {
            lbMess.setText("Sđt không đúng");
        } else if (txtAddress.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập địa chỉ");
        } else if (txtWT.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập thời gian làm việc");
        } else if (!v.CheckNumeric(txtWT.getText())) {
            lbMess.setText("Vui lòng nhập số");
        } else if (txtRate.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập hệ số lương");
        } else if (!v.CheckNumeric(txtRate.getText())) {
            lbMess.setText("Vui lòng nhập số");
        } else if (txtSalary.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập lương");
        } else if (!v.CheckNumeric(txtSalary.getText())) {
            lbMess.setText("Vui lòng nhập số");
        } else if (txtPass.getText().trim().isEmpty()) {
            lbMess.setText("Vui lòng nhập mật khẩu");
        } else {
            String name = txtName.getText();
            int age = Integer.parseInt(txtAge.getText());
            String email = txtEmail.getText();
            String phone = txtPhone.getText();
            String address = txtAddress.getText();
            int roleId = ((Role) cbxRole.getSelectedItem()).getId();

            float workTime = Float.parseFloat(txtWT.getText());
            float rate = Float.parseFloat(txtRate.getText());
            float salary = Float.parseFloat(txtSalary.getText());
            String password = txtPass.getText();
            boolean status = true;
            if (jRadioButton1.isSelected()) {
                status = true;
            } else {
                status = false;
            }
            Employee e = new Employee(name, age, email, phone, address, roleId, new Date(startDate.getDate().getTime()), workTime, rate, salary, password, status);
            EmployeeDAOImp empDAO = new EmployeeDAOImp();
            empDAO.add(e);
            loadList(null);
        }
        // TODO add your handling code here:

    }//GEN-LAST:event_btnAddActionPerformed

